public class EstruturaRepeticao {
    public static void main(String[] args) {

        for(int i=1; i<=5; i++) {
            System.out.println("Contador:"+i);
        }

        System.out.println("");

        int contador = 0;
        while (contador < 5) {
            contador++;
            System.out.println("Contador: "+contador);
        }

    }
}