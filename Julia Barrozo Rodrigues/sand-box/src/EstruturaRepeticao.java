public class EstruturaRepeticao {
    public static void main(String[] args) {
        
        // i++ = i + 1

        for(int i=1; i<5; i++) {
            System.out.println("valor: " + i);
        }

        int contador = 0;
        while (contador < 5) {
            System.out.println("contador: " + contador);
            contador++;
        }

        // Proxima aula lista e for melhor
    }
}
