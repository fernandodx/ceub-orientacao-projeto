public class EstruturaRepeticao {

    public static void main(String[] args) {
        
        //for
        for (int i = 1; i <= 5; i++) {
            System.out.println("vlor: " + i);
        }

        // lista?

        //while
        int contador = 0;
        while (contador < 5) {
            System.out.println("contador: " + contador);
            contador++;
        }
    }
}
