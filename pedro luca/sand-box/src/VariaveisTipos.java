public class TiposDeDadosDemo {

    public static void main(String[] parametros) {

        /*
         * Demonstração de diferentes tipos de variáveis em Java.
         *
         * Tipos primitivos → iniciam com letra minúscula.
         * Objetos (como String) → iniciam com letra maiúscula.
         */

        int quantidade = 42;
        double valorUnitario = 15.75;
        char inicial = 'R';
        boolean statusAtivo = false;
        String mensagem = "Bem-vindo!";
        boolean estaDeFolga = true;

        System.out.println(quantidade);
        System.out.println(valorUnitario);
        System.out.println(inicial);
        System.out.println(statusAtivo);
        System.out.println(estaDeFolga);
        System.out.println(mensagem);
    }
}